// Obtener producto por ID
export const getProduct = (id) => {
	return new Promise((resolve, reject) => {
		setTimeout(() => {
			const product = dataProducts.find((product) => product.id == id);
			product ? resolve(product) : reject("Producto no encontrado");
		}, 1000);
	});
};

// Obtener todos los productos de la categoría seleccionada
export const getProducts = (category) => {
	return new Promise((resolve, reject) => {
		setTimeout(() => {
			if (!category) { // Si no hay categoría, devolver todos
				resolve(dataProducts);
			} else if (typeof category === "string") {
				const filteredProducts = dataProducts.filter(
					(product) => product.category === category
				);
				resolve(filteredProducts);
			} else {
				reject("Categoría no encontrada");
			}
		}, 1000);
	});
};
